	<h1>Parque de la Memoria y Parque Natural</h1>
	<div class="noticia-viendo">
		<p><strong>Parque y Monumento: Arquitectura y Naturaleza en el Paisaje Costero de Buenos Aires</strong></p>

		<p>La ocupación de “terrenos vagos” como los que rodean los pabellones de la Ciudad Universitaria, la reinserción urbana de fragmentos monofuncionales –como el campus- y la reconstrucción del paisaje urbano contemporáneo de la ciudad son parte de los nuevos que afronta Buenos Aires junto a la renovación de sus infraestructuras y la reconstrucción de los intersticios del tejido en los barrios consolidados de la ciudad.</p>
		<p>En Buenos Aires recién se han iniciado las necesarias intervenciones contemporáneas en el paisaje urbano y natural, la costa y los nuevos espacios abiertos. La concepción estereotipada de que las grandes intervenciones paisajísticas de fin de siglo XIX y principios del XX – como los Bosques de Palermo o la Costanera Sur – podían o debían ser imitadas sólo habría conducido a una nostalgia del pasado que hubiera reflejado una falta de confianza en el futuro.</p>
		<p>Dado que estos nuevos parques urbanos albergarán actividades masivas y que, simultáneamente, en general existen pocos espacios públicos al aire libre a los que la población pueda acceder, la propuesta realizada respeta el medio ambiente natural de manera que estos espacios estén disponibles para el uso y disfrute del espacio urbano activo que los ciudadanos actualmente tanto reclaman.</p>
		<p>Para que esta demanda tenga respuesta, las intervenciones de diseño paisajístico y urbano en el área deben permitir una reconsideración de la relación entre lo natural y lo artificial en la que la convivencia entre la vida urbana y la vida en contacto con la naturaleza – o con su representación – se convierta en uno de los valores de la cultura de la ciudad y, además, una no vaya en detrimento de la otra.</p>
		<p>Por esta razón, los parques proyectados tratan de encontrar un equilibrio entre la presencia de la naturaleza – el paisaje natural – y la presencia de la ciudad con sus infraestructuras, usos recreativos, arquitectura y paseos públicos. Pero, por sobre todo, y como en toda intervención de gran escala, tratan de resolver la identidad del sitio dentro de una concepción que valore ajustadamente los elementos naturales como el factor pedagógico (…)</p>
		<p>En cuanto al Parque de la Memoria, la recaptación de este vacío tan importante para el uso público y para un programa de recuperación de la memoriarefleja el papel emblemático del uso social del territorio. Debido al alto grado de deterioro territorial que tenía el borde ribereño de la Ciudad Universitaria y sus áreas aledañas, y el beneficio social que se obtiene de una intervención como la que se plantea, no cabe duda de que convierte este destino en uno de los mayores desafíos del presente proyecto.</p>
		<p>En el caso particular del Parque de la Memoria, la incorporación estratégica de esta pieza en el conjunto de los parques aporta una referencia de significado que dota de “historicidad” a una zona que ha sido un borde anónimo de la ciudad y, a su vez, testigo presencial de las horas más trágicas de la historia argentina.</p>
		<p>Ha sido esencial en la formulación de estos proyectos la decisión de incorporar en su programa, como elemento fundamental, los monumentos referidos a sucesos desgraciados de la historia argentina reciente, cuya memoria se ha incorporado al diseño del parque y del monumento, insertándolos como elementos generadores de una costa reciclada de escala monumental.</p>
		<p>La idea de tematizar estos territorios indefinidos mediante un tratamiento paisajístico acorde con la presencia del monumento planteó el segundo gran desafío del proyecto de los parques: dotar el espacio urbano y el borde costero de una escala y una naturaleza acorde a su nueva dimensión significativa.</p>
		<p>[...]</p>
		<p>El Parque de la Memoria de Buenos Aires y el Monumento a las Víctimas del Terrorismo de Estado son obras ofrecidas a quienes han conocido los trágicos orígenes que motivaron su construcción como un instrumento de catarsis, como materialización de la memoria; pero también, como otros grandes memoriales del mundo, se ofrece a la humanidad por la dimensión universal de su significado como una contribución de nuestro país al campo de la arquitectura y de las grandes intervenciones territoriales contemporáneas, como una pieza urbana de gran contenido.</p>
		<p>En este aspecto, el alcance universal de la obra la coloca en un diálogo con las grandes arquitecturas de la memoria. El monumento forma parte del paisaje, del cielo y del río, y, a la vez, configura el paisaje. Allí donde alguna vez se arrojaron personas al río y donde el territorio no existía se creó un paisaje de rememoración y contacto con las cosas más permanentes de la existencia: la tierra, el agua, el horizonte, la ciudad, las cosas que no pueden ser removidas y que son los materiales de la arquitectura del monumento y el parque. Así, el monumento, el parque y el paisaje se han fusionado en una misma y única visión.</p>

		<p>Por Alberto Varas.</p>

