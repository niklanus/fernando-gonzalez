	<h1>Club de Campo Aguavista, el paisajismo como concepto arquitectónico y medioambiental</h1>
	<div class="noticia-viendo">
		<p><strong>Uno de los proyectos regionales que amalgama los conceptos del medio ambiente y paisajismo es el Club de Campo Residencial AguaVista, ubicado en la localidad de San Juan del Paraná, Paraguay, a escasos kilómetros de Encarnación y Posadas. Allí se logra una interrelación entre el cuidado del medio ambiente y el bienestar de futuras generaciones en un vínculo directo con el ecosistema.</strong></p>

		<p>(Por la licenciada Eliana Levitt). El desarrollo a través de los proyectos arquitectónicos de gran envergadura incorpora cada vez con mayor frecuencia y compromiso el concepto del paisajismo, considerado como la práctica de ordenación de las actividades humanas para relacionar estrechamente el territorio, el medio ambiente, el urbanismo y el paisaje. Y cuando se habla de dichas intervenciones en una región que incorpora un bastión verde como es la Selva Paranaense (una de las mayores superficies naturales del mundo y la segunda reserva de biodiversidad de América) el compromiso es todavía mayor.</p>

		<p>De ese modo se logra una interrelación entre el cuidado del medio ambiente y el bienestar de futuras generaciones en un vínculo directo con el ecosistema.</p>
		<p>Uno de los proyectos regionales que amalgama los conceptos del medio ambiente y paisajismo es el Club de Campo Residencial AguaVista, ubicado en la localidad de San Juan del Paraná (Paraguay), a escasos kilómetros de Encarnación y Posadas y a orillas del río Paraná.</p>
		<p>La tarea fue encomendada a Fernando González, un reconocido profesional, con 30 años de experiencia, que se especializa en la realización de proyectos de arquitectura del paisaje de mediana y gran envergadura.</p>
		<p>Apenas el proyecto comenzó a tomar forma, cada socio fundador de AguaVista plantó un árbol dando origen al Bosque de los Fundadores, símbolo de la conciencia social y de la responsabilidad asumida ante el desafío de los constantes cambios climáticos que sufre el planeta.</p>
		<p>Y desde el inicio mismo se definió que el paisaje sirve de materia prima al hombre; este toma prestados los elementos que se encuentran en la naturaleza, los rescata, los conserva, les da forma, y los integra armonizándolos en un todo.</p>
		<p>Por todo esto, AguaVista está planificando, proyectando, construyendo y conservando los elementos que conforman el paisaje natural, tratando de combinarlos con lo creado por el hombre, de tal forma que todo se conjugue en armonía.</p>


		<p class="subtitle">Una acción integradora: belleza en cada estación</p>

		<p>González explicó que "se da especial énfasis a la vegetación, potenciando la que ya existe en AguaVista. Se busca incorporar especies en cantidad y calidad de belleza, verificando diversos factores como ser tiempo de crecimiento, floración, etc. A continuación, se hace un análisis para que existan efectos bellos durante todas las temporadas del año, en las distintas estaciones".</p>
		<p>De esa manera, agrega, "todo quedará como algo espontáneo, siendo que fue perfectamente planificado, la disposición de los elementos naturales, piedras, maderas, desagües, el pastizal, la cancha de golf, las casas, todo en los más mínimos detalles. Con el tiempo llegará a ser natural, inherente, cuando en realidad fue pensado y calculado". Y AguaVista encajará como un gigantesco rompecabezas.</p>
		<p>Justamente, las primeras viviendas construidas (hay cuatro casas finalizadas y que son disfrutadas los fines de semanas por sus dueños) se agrupan en el barrio Bauhinias Residenciales, nombre científico de la orquídea. Recibe este nombre porque será el único que tendrá orquídeas blancas y rosadas. [...]</p>

